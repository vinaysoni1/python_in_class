# Q1. Find the intersection of two sets
A={1,2,3,4,5}
B={4,5,6,7,8}
print("intersection:", A.intersection(B)) # Intersection of A and B

# Q2. not present in both sets
A={10,20,30,40}
B={30,40,50,60}
print("symmetric_difference:", A.symmetric_difference(B)) # Symmetric difference of A and B

s1={10, 20, 30}
s2={40, 50, 60}
s3={70, 80, 90}
print("union of s1, s2 and s3:", s1.union(s2, s3)) # Union of s1, s2 and s3
